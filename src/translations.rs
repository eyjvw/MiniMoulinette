#[derive(Clone, Copy, PartialEq, Eq, Debug)]
pub enum Lang {
    En,
    Fr,
    Es,
}

impl Lang {
    pub fn parse(s: &str) -> Option<Lang> {
        match s.to_lowercase().as_str() {
            "en" | "english" | "anglais" | "ingles" | "inglés" => Some(Lang::En),
            "fr" | "french" | "francais" | "français" | "frances" | "francés" => Some(Lang::Fr),
            "es" | "spanish" | "espagnol" | "espanol" | "español" => Some(Lang::Es),
            _ => None,
        }
    }

    pub fn code(&self) -> &'static str {
        match self {
            Lang::En => "en",
            Lang::Fr => "fr",
            Lang::Es => "es",
        }
    }
}

pub fn fill(tpl: &str, args: &[&str]) -> String {
    let mut s = tpl.to_string();
    for a in args {
        s = s.replacen("{}", a, 1);
    }
    s
}

